#!/bin/bash
# ============================================================
# 프로덕션 API 서버 시작 스크립트
# 순서: 스키마 마이그레이션 → 서버 시작
# ============================================================
set -e

echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
echo "🚀 Edubee API 서버 프로덕션 시작"
echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"

# ── 1. 데이터베이스 스키마 자동 업데이트 ─────────────────────
echo "[1/2] DB 스키마 업데이트 중 (drizzle push)..."
if pnpm --filter db push --accept-data-loss 2>&1; then
  echo "✅ 스키마 업데이트 완료"
else
  echo "⚠️  스키마 업데이트 실패 — 서버를 계속 시작합니다 (스키마가 이미 최신 상태일 수 있음)"
fi

echo ""

# ── 2. API 서버 시작 ─────────────────────────────────────────
echo "[2/2] API 서버 시작 중..."
exec node artifacts/api-server/dist/index.cjs
