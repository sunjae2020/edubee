// ─── Types ───────────────────────────────────────────────────────────────────
interface PaymentHeader {
  id: string;
  paymentRef?: string | null;
  paymentDate: string;
  paymentType: string;
  paymentMethod?: string | null;
  totalAmount?: string | null;
  bankReference?: string | null;
  notes?: string | null;
  status: string;
  createdOn: string;
}

interface JournalEntry {
  id: string;
  entryDate: string;
  paymentHeaderId?: string | null;
  paymentRef?: string | null;
  paymentStatus?: string | null;
  debitCoa: string;
  debitCoaName?: string;
  creditCoa: string;
  creditCoaName?: string;
  amount: string;
  entryType?: string | null;
  contractId?: string | null;
  autoGenerated: boolean;
  createdOn: string;
}

interface SplitLine {
  uid: string;
  contractProductId: string;
  coaCode: string;
  splitType: string;
  amount: string;
  description: string;
}

// ─── Constants ───────────────────────────────────────────────────────────────
const PAYMENT_TYPES: Array<{ key: string; label: string }> = [
  { key: "trust_receipt",  label: "Trust Receipt"  },
  { key: "trust_transfer", label: "Trust Transfer" },
  { key: "commission",     label: "Commission"     },
  { key: "direct",         label: "Direct"         },
];

const PAYMENT_METHODS = ["Bank Transfer", "Cash", "Card", "Cheque", "EFT"];

const JE_PREVIEW: Record<string, { debit: string; credit: string; debitLabel: string; creditLabel: string }> = {
  trust_receipt:  { debit: "1200", credit: "1300", debitLabel: "Trust Account",                creditLabel: "AR — Commission Receivable"   },
  trust_transfer: { debit: "2100", credit: "1200", debitLabel: "AP — Student Tuition Payable", creditLabel: "Trust Account"                },
  commission:     { debit: "1100", credit: "3100", debitLabel: "Cash & Bank — Operating",      creditLabel: "Commission — Tuition"         },
  direct:         { debit: "4700", credit: "1100", debitLabel: "Direct — Other",               creditLabel: "Cash & Bank — Operating"      },
};

const STATUS_STYLE: Record<string, { bg: string; text: string }> = {
  Active: { bg: "#DCFCE7", text: "#16A34A" },
  Void:   { bg: "#FEF2F2", text: "#DC2626" },
};

const TYPE_STYLE: Record<string, string> = {
  trust_receipt:  "#FEF0E3",
  trust_transfer: "#FEF9C3",
  commission:     "#DCFCE7",
  direct:         "#F4F3F1",
};

// ─── Helpers ─────────────────────────────────────────────────────────────────
function fmtAmt(v: string | null | undefined): string {
  if (!v) return "—";
  return `A$${Number(v).toLocaleString("en-AU", { minimumFractionDigits: 2 })}`;
}
function fmtDate(d: string | null | undefined): string {
  if (!d) return "—";
  try { return format(new Date(d), "MMM d, yyyy"); } catch { return d; }
}
function parseNotes(notes: string | null | undefined): { receivedFromName?: string; paidToName?: string; notes?: string } {
  if (!notes) return {};
  try { return JSON.parse(notes); } catch { return { notes: notes ?? undefined }; }
}
function newLine(): SplitLine {
  return { uid: crypto.randomUUID(), contractProductId: "", coaCode: "", splitType: "principal", amount: "", description: "" };
}

// ─── Void Action dropdown ────────────────────────────────────────────────────
function VoidAction({ paymentId, status, isSuperAdmin }: { paymentId: string; status: string; isSuperAdmin: boolean }) {
  const [open, setOpen] = useState(false);
  const { toast } = useToast();
  const qc = useQueryClient();

  const voidMutation = useMutation({
    mutationFn: () => axios.patch(`${BASE}/api/accounting/payments/${paymentId}/void`).then(r => r.data),
    onSuccess: () => {
      qc.invalidateQueries({ queryKey: ["payments"] });
      qc.invalidateQueries({ queryKey: ["journal-entries"] });
      toast({ title: "Payment voided" });
      setOpen(false);
    },
    onError: () => toast({ title: "Void failed", variant: "destructive" }),
  });

  if (!isSuperAdmin || status === "Void") return null;

  return (
    <div className="relative inline-block">
      <button onClick={() => setOpen(o => !o)}
        className="flex items-center gap-1 px-2 py-1 rounded border border-stone-200 text-xs text-stone-600 hover:bg-stone-50">
        <ChevronDown size={11} />
      </button>
      {open && (
        <>
          <div className="fixed inset-0 z-10" onClick={() => setOpen(false)} />
          <div className="absolute right-0 top-7 z-20 w-36 bg-white border border-stone-200 rounded-xl shadow-lg py-1">
            <button
              onClick={() => voidMutation.mutate()}
              disabled={voidMutation.isPending}
              className="w-full text-left px-3 py-2 text-sm text-red-600 hover:bg-red-50"
            >
              {voidMutation.isPending ? "Voiding…" : "Void Payment"}
            </button>
          </div>
        </>
      )}
    </div>
  );
}

