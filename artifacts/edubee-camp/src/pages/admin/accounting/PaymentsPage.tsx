// ─── Create Payment Sheet ─────────────────────────────────────────────────────
function CreateSheet({ onClose }: { onClose: () => void }) {
  const { toast } = useToast();
  const qc = useQueryClient();

  const [paymentType,      setPaymentType]      = useState("trust_receipt");
  const [paymentDate,      setPaymentDate]      = useState(new Date().toISOString().slice(0, 10));
  const [paymentMethod,    setPaymentMethod]    = useState("Bank Transfer");
  const [receivedFromName, setReceivedFromName] = useState("");
  const [paidToName,       setPaidToName]       = useState("");
  const [bankReference,    setBankReference]    = useState("");
  const [notes,            setNotes]            = useState("");
  const [lines,            setLines]            = useState<SplitLine[]>([newLine()]);

  const total = lines.reduce((s, l) => s + Number(l.amount || 0), 0);
  const je    = JE_PREVIEW[paymentType];

  function updateLine<K extends keyof SplitLine>(uid: string, key: K, val: SplitLine[K]) {
    setLines(prev => prev.map(l => l.uid === uid ? { ...l, [key]: val } : l));
  }

  const createMutation = useMutation({
    mutationFn: () => axios.post(`${BASE}/api/accounting/payments`, {
      paymentDate, paymentMethod, paymentType,
      bankReference: bankReference || null,
      notes: notes || null,
      receivedFromName: receivedFromName || null,
      paidToName: paidToName || null,
      lines: lines.map(l => ({
        contractProductId: l.contractProductId || null,
        coaCode:           l.coaCode || null,
        splitType:         l.splitType || null,
        amount:            Number(l.amount || 0),
        description:       l.description || null,
      })),
    }).then(r => r.data),
    onSuccess: () => {
      qc.invalidateQueries({ queryKey: ["payments"] });
      qc.invalidateQueries({ queryKey: ["journal-entries"] });
      qc.invalidateQueries({ queryKey: ["ar-summary"] });
      qc.invalidateQueries({ queryKey: ["ar"] });
      qc.invalidateQueries({ queryKey: ["ap"] });
      toast({ title: "Payment created + Journal Entry generated" });
      onClose();
    },
    onError: () => toast({ title: "Payment failed", variant: "destructive" }),
  });

  return (
    <div className="fixed inset-0 z-50 flex justify-end bg-black/30 backdrop-blur-sm">
      <div className="w-full max-w-lg bg-white shadow-2xl flex flex-col h-full overflow-hidden">
        {/* Header */}
        <div className="flex items-center justify-between px-5 py-4 border-b border-stone-200 shrink-0">
          <h3 className="text-base font-bold text-stone-800">Create Payment</h3>
          <button onClick={onClose} className="p-1.5 rounded hover:bg-stone-100">
            <X size={16} className="text-stone-400" />
          </button>
        </div>

        <div className="flex-1 overflow-y-auto p-5 space-y-5">
          {/* Payment Type */}
          <div className="space-y-2">
            <Label className="text-xs font-medium text-stone-600">Payment Type</Label>
            <div className="flex gap-2 flex-wrap">
              {PAYMENT_TYPES.map(t => (
                <button
                  key={t.key}
                  onClick={() => setPaymentType(t.key)}
                  className={`px-3 py-1.5 rounded-lg text-xs font-medium border transition-all ${
                    paymentType === t.key
                      ? "border-[#F5821F] bg-[#FEF0E3] text-[#F5821F]"
                      : "border-stone-200 text-stone-600 hover:border-stone-400"
                  }`}
                >
                  {t.label}
                </button>
              ))}
            </div>
          </div>

          {/* Core Fields */}
          <div className="grid grid-cols-2 gap-3">
            <div className="space-y-1.5">
              <Label className="text-xs font-medium text-stone-600">Payment Date</Label>
              <Input type="date" value={paymentDate} onChange={e => setPaymentDate(e.target.value)}
                className="h-9 text-sm" />
            </div>
            <div className="space-y-1.5">
              <Label className="text-xs font-medium text-stone-600">Payment Method</Label>
              <Select value={paymentMethod} onValueChange={setPaymentMethod}>
                <SelectTrigger className="h-9 text-sm"><SelectValue /></SelectTrigger>
                <SelectContent>
                  {PAYMENT_METHODS.map(m => <SelectItem key={m} value={m}>{m}</SelectItem>)}
                </SelectContent>
              </Select>
            </div>
            <div className="space-y-1.5">
              <Label className="text-xs font-medium text-stone-600">Received From</Label>
              <Input value={receivedFromName} onChange={e => setReceivedFromName(e.target.value)}
                placeholder="Name / company…" className="h-9 text-sm" />
            </div>
            <div className="space-y-1.5">
              <Label className="text-xs font-medium text-stone-600">Paid To</Label>
              <Input value={paidToName} onChange={e => setPaidToName(e.target.value)}
                placeholder="Name / company…" className="h-9 text-sm" />
            </div>
            <div className="col-span-2 space-y-1.5">
              <Label className="text-xs font-medium text-stone-600">Bank Reference</Label>
              <Input value={bankReference} onChange={e => setBankReference(e.target.value)}
                placeholder="e.g. BSB/Account, transfer ref…" className="h-9 text-sm" />
            </div>
          </div>

          {/* Split Lines */}
          <div className="space-y-2">
            <Label className="text-xs font-medium text-stone-600 uppercase tracking-wide">Split Lines</Label>
            <div className="border border-stone-200 rounded-xl overflow-hidden">
              <table className="w-full text-xs">
                <thead className="bg-stone-50 border-b border-stone-100">
                  <tr>
                    <th className="text-left px-3 py-2 text-stone-400 font-medium">Invoice / Contract Product ID</th>
                    <th className="text-left px-3 py-2 text-stone-400 font-medium w-20">COA</th>
                    <th className="text-left px-3 py-2 text-stone-400 font-medium w-24">Split Type</th>
                    <th className="text-left px-3 py-2 text-stone-400 font-medium w-24">Amount (AUD)</th>
                    <th className="w-7" />
                  </tr>
                </thead>
                <tbody className="divide-y divide-stone-50">
                  {lines.map(line => (
                    <tr key={line.uid}>
                      <td className="px-2 py-1.5">
                        <Input
                          value={line.contractProductId}
                          onChange={e => updateLine(line.uid, "contractProductId", e.target.value)}
                          className="h-7 text-xs border-stone-200" placeholder="UUID (optional)" />
                      </td>
                      <td className="px-2 py-1.5">
                        <Input
                          value={line.coaCode}
                          onChange={e => updateLine(line.uid, "coaCode", e.target.value)}
                          className="h-7 text-xs border-stone-200 font-mono" placeholder="1200" />
                      </td>
                      <td className="px-2 py-1.5">
                        <Select
                          value={line.splitType}
                          onValueChange={v => updateLine(line.uid, "splitType", v)}
                        >
                          <SelectTrigger className="h-7 text-xs border-stone-200">
                            <SelectValue />
                          </SelectTrigger>
                          <SelectContent>
                            {["principal", "commission", "fee", "tax", "other"].map(s => (
                              <SelectItem key={s} value={s}>{s}</SelectItem>
                            ))}
                          </SelectContent>
                        </Select>
                      </td>
                      <td className="px-2 py-1.5">
                        <Input
                          type="number" min={0} step="0.01"
                          value={line.amount}
                          onChange={e => updateLine(line.uid, "amount", e.target.value)}
                          className="h-7 text-xs border-stone-200" placeholder="0.00" />
                      </td>
                      <td className="px-2 py-1.5">
                        <button onClick={() => setLines(prev => prev.filter(l => l.uid !== line.uid))}
                          className="p-0.5 rounded hover:bg-red-50 text-stone-300 hover:text-red-500">
                          <Trash2 size={12} />
                        </button>
                      </td>
                    </tr>
                  ))}
                </tbody>
              </table>
              <div className="px-3 py-2 flex items-center justify-between border-t border-stone-100">
                <button onClick={() => setLines(prev => [...prev, newLine()])}
                  className="flex items-center gap-1 text-xs text-[#F5821F] hover:underline font-medium">
                  <Plus size={12} /> Add Line
                </button>
                <span className="text-xs font-bold text-stone-800">
                  Total: A${total.toLocaleString("en-AU", { minimumFractionDigits: 2 })}
                </span>
              </div>
            </div>
          </div>

          {/* JE Preview */}
          {je && (
            <div className="bg-stone-50 border border-stone-200 rounded-xl p-4 space-y-2">
              <div className="flex items-center gap-2 text-xs font-semibold text-stone-600 uppercase tracking-wide">
                <BookOpen size={13} /> Journal Entry Preview
              </div>
              <div className="space-y-1 font-mono text-sm">
                <div className="flex justify-between">
                  <span className="text-stone-500">DR <span className="text-stone-800">{je.debit}</span> {je.debitLabel}</span>
                  <span className="font-semibold text-stone-800">
                    A${total.toLocaleString("en-AU", { minimumFractionDigits: 2 })}
                  </span>
                </div>
                <div className="flex justify-between pl-4">
                  <span className="text-stone-500">CR <span className="text-stone-800">{je.credit}</span> {je.creditLabel}</span>
                  <span className="font-semibold text-stone-800">
                    A${total.toLocaleString("en-AU", { minimumFractionDigits: 2 })}
                  </span>
                </div>
              </div>
            </div>
          )}

          {/* Notes */}
          <div className="space-y-1.5">
            <Label className="text-xs font-medium text-stone-600">Notes</Label>
            <Textarea value={notes} onChange={e => setNotes(e.target.value)}
              className="text-sm min-h-[60px] resize-none" placeholder="Internal notes…" />
          </div>
        </div>

        {/* Footer */}
        <div className="px-5 py-4 border-t border-stone-200 shrink-0">
          <Button
            onClick={() => createMutation.mutate()}
            disabled={createMutation.isPending}
            className="w-full text-white flex items-center justify-center gap-2"
            style={{ background: "#F5821F" }}
          >
            <ChevronsRight size={15} />
            {createMutation.isPending ? "Creating…" : "Save & Create Journal Entry"}
          </Button>
        </div>
      </div>
    </div>
  );
}

